// cria a classe da calculadora 
class Calculadora {
  
  // funcão para somar 
  somar(num1, num2) {
    return num1 + num2;
  }

  // funcao para subtrair 
  subtrair(num1,num2) {
    return num1 - num2;
  }

  // funcao para multiplicar  
  multiplicar (num1,num2) {
    return num1 * num2;
  }

  // funcao para dividir
  dividir (num1,num2) {
    return num1 / num2;
  }


  // funcao da potência 
  potencia (num1,num2) {
    return Math.pow(num1,num2);
  }

  // funcao de raizQuadrada 
  raizQuadrada (num1,num2) {
    return Math.sqrt(num1,num2);
  }

}


